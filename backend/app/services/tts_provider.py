import logging
from abc import ABC, abstractmethod
from pathlib import Path

logger = logging.getLogger(__name__)


class TTSProvider(ABC):
    @abstractmethod
    async def synthesize(self, text: str, instructions: str, output_path: Path) -> None:
        """Synthesize speech from text and write mp3 to output_path."""
        ...

    @abstractmethod
    async def close(self) -> None:
        ...


